app.post('/auth/login', async (req, res) => {
  try {
    const { email, password } = req.body;
    const account = await Account.findOne({ email });

    if (!account) {
      return res.render('index.hbs', { error: 'Invalid email or password' });
    }

    const isMatch = await bcrypt.compare(password, account.password);
    if (!isMatch) {
      return res.render('index.hbs', { error: 'Invalid email or password' });
    }

    // Set the user's session
    req.session.account = account;

    // Set the "remember me" cookie if the user checked the checkbox
    if (req.body.rememberMe) {
      const rememberToken = await generateRememberToken(account);
      res.cookie('rememberToken', rememberToken, { maxAge: 3 * 30 * 24 * 60 * 60 * 1000, httpOnly: true }); // 3 weeks
    }

    res.redirect('/home');
  } catch (error) {
    console.error('Error logging in:', error);
    res.render('index.hbs', { error: 'An error occurred during login' });
  }
});

async function generateRememberToken(account) {
  const rememberToken = await bcrypt.hash(`${account._id}:${Date.now()}`, 10);
  return rememberToken;
}

